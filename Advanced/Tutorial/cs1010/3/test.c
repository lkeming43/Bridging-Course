#include <stdio.h>

int main(void) {
    int x, result;

    /* 演示 && 的短路与自增顺序 */
    printf("=== 演示 AND (&&) 的短路与 x++ ===\n");
    // 情况 1：初始 x = 0
    x = 0;
    result = (x++ && x++);
    // 解释：
    //  1) x++ 先返回旧值 0（假），x 增为 1；
    //  2) 因为是假，短路，不再执行第二个 x++；
    //  3) 整个表达式返回 0（假），最终 x=1。
    printf("初始 x=0: (x++ && x++)→result=%d, 最终 x=%d\n", result, x);

    // 情况 2：初始 x = 1
    x = 1;
    result = (x++ && x++);
    // 解释：
    //  1) x++ 返回旧值 1（真），x→2；
    //  2) 因为是真，继续执行第二个 x++，返回旧值 2（真），x→3；
    //  3) 整个表达式返回 1（真），最终 x=3。
    printf("初始 x=1: (x++ && x++)→result=%d, 最终 x=%d\n\n", result, x);


    /* 演示 || 的短路与自增顺序 */
    printf("=== 演示 OR (||) 的短路与 x++ ===\n");
    // 情况 3：初始 x = 0
    x = 0;
    result = (x++ || x++);
    // 解释：
    //  1) x++ 返回旧值 0（假），x→1；
    //  2) 因为是假，继续执行第二个 x++，返回旧值 1（真），x→2；
    //  3) 整个表达式返回 1（真），最终 x=2。
    printf("初始 x=0: (x++ || x++)→result=%d, 最终 x=%d\n", result, x);

    // 情况 4：初始 x = 1
    x = 1;
    result = (x++ || x++);
    // 解释：
    //  1) x++ 返回旧值 1（真），x→2；
    //  2) 因为是真，短路，不再执行第二个 x++；
    //  3) 整个表达式返回 1（真），最终 x=2。
    printf("初始 x=1: (x++ || x++)→result=%d, 最终 x=%d\n", result, x);

    return 0;
}